class GreaterThree1
{
    public static void main(String[] args)
    {
        int a=10, b=20, c=30;
        if(a>b && a>c)
        {
            System.out.println("a is Greater");
        }else if(b>a && b>c)
        {
            System.out.println("b is Greater");
        }else
        {
            System.out.println("c is Greater");
        }
    }
}